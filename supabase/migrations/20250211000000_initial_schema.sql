  using (
    exists (select 1 from public.user_roles where user_id = auth.uid() and role = 'admin')
  );

-- 8. RLS policies: announcements
-- -------------------------------
-- Public read. Admin: full access. Club admin: only rows where club_id = their club_id.

create policy "Anyone can read announcements"
  on public.announcements for select
  using (true);

create policy "Admin can insert announcements"
  on public.announcements for insert
  with check (
    exists (select 1 from public.user_roles where user_id = auth.uid() and role = 'admin')
  );

-- Club admin can only insert announcements for their club (new row's club_id must match their club_id)
create policy "Club admin can insert own club announcements"
  on public.announcements for insert
  with check (
    exists (
      select 1 from public.user_roles ur
      where ur.user_id = auth.uid() and ur.role = 'club_admin' and ur.club_id is not null and ur.club_id = announcements.club_id
    )
  );

create policy "Admin can update announcements"
  on public.announcements for update
  using (
    exists (select 1 from public.user_roles where user_id = auth.uid() and role = 'admin')
  );

create policy "Club admin can update own club announcements"
  on public.announcements for update
  using (
    exists (
      select 1 from public.user_roles ur
      where ur.user_id = auth.uid() and ur.role = 'club_admin' and ur.club_id = announcements.club_id
    )
  );

create policy "Admin can delete announcements"
  on public.announcements for delete
  using (
    exists (select 1 from public.user_roles where user_id = auth.uid() and role = 'admin')
  );

create policy "Club admin can delete own club announcements"
  on public.announcements for delete
  using (
    exists (
      select 1 from public.user_roles ur
      where ur.user_id = auth.uid() and ur.role = 'club_admin' and ur.club_id = announcements.club_id
    )
  );

