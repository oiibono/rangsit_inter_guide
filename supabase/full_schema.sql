CREATE TABLE IF NOT EXISTS public.clubs (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name text NULL,
  description text NULL,
  image_url text NULL,
  created_at timestamptz NOT NULL DEFAULT (now() AT TIME ZONE 'utc'::text),
  managed_by text NULL,
  CONSTRAINT clubs_pkey PRIMARY KEY (id)
) TABLESPACE pg_default;

CREATE TABLE IF NOT EXISTS public.announcements (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  title text NOT NULL,
  subtitle text NULL,
  image_url text NOT NULL DEFAULT 'https://stock.adobe.com/th/search?k=image'::text,
  date date NULL DEFAULT now(),
  see_more_url text NULL,
  category text NULL,
  created_at timestamptz NULL,
  club_id bigint NULL,
  created_by_role text NULL CHECK (created_by_role IN ('admin', 'club_admin')),
  created_by_user_id uuid NULL REFERENCES auth.users (id) ON DELETE SET NULL,
  CONSTRAINT announcements_pkey PRIMARY KEY (id),
  CONSTRAINT announcements_club_id_fkey FOREIGN KEY (club_id) REFERENCES public.clubs (id)
) TABLESPACE pg_default;

CREATE TABLE IF NOT EXISTS public.club_announcements (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  title text NOT NULL,
  subtitle text NULL,
  image_url text NOT NULL DEFAULT 'https://stock.adobe.com/th/search?k=image'::text,
  date date NULL DEFAULT now(),
  see_more_url text NULL,
  category text NULL,
  created_at timestamptz NULL DEFAULT now(),
  club_id bigint NOT NULL REFERENCES public.clubs (id) ON DELETE CASCADE,
  created_by_role text NULL CHECK (created_by_role IN ('admin', 'club_admin')),
  created_by_user_id uuid NULL REFERENCES auth.users (id) ON DELETE SET NULL,
  CONSTRAINT club_announcements_pkey PRIMARY KEY (id),
  CONSTRAINT club_announcements_club_id_fkey FOREIGN KEY (club_id) REFERENCES public.clubs (id) ON DELETE CASCADE
) TABLESPACE pg_default;

CREATE TABLE IF NOT EXISTS public.user_roles (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  user_id uuid NOT NULL REFERENCES auth.users (id) ON DELETE CASCADE,
  role text NOT NULL CHECK (role IN ('admin', 'club_admin')),
  club_id bigint NULL REFERENCES public.clubs (id) ON DELETE SET NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT user_roles_pkey PRIMARY KEY (id),
  CONSTRAINT user_roles_user_id_key UNIQUE (user_id)
) TABLESPACE pg_default;

