CREATE TABLE IF NOT EXISTS public.international_programs (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  image_url text NULL DEFAULT 'https://stock.adobe.com/search?k=no+image'::text,
  name_line1 text NULL,
  name_line2 text NULL,
  CONSTRAINT international_programs_pkey PRIMARY KEY (id)
